/*
    Type conversion (typecasting) vs Type coersion
    
    * Alteração de um tipo de dado para outro tipo
*/

console.log('9' + 5); /* + serve para somar OU concatenar valores 
                    (coersion = transformou um tipo number num tipo 
                    string para que o programa não tenha nenhum programa) */

console.log(Number('9') + 5); // conversion nós implicitamente trocamos o tipo do dado usando uma function