// Operadores de atribuição (Assignment)

let x;
console.log(x);

// assignment
x = 1;
console.log(x);

// addition assignment
x += 2; // mesma coisa que x = x + 2;
console.log(x);

// subtraction assignment
x -= 1; // x = x - 1;
console.log(x);

// multiplication assignment
x *= 2; // x = x * 2;
console.log(x);

// division assignment
x /= 2; // x = x / 2;
console.log(x);

// remainder, exponetiation assignment
x %= 2; 
x **= 2;
console.log(x);