// Operadores Aritiméticos

// Multiplicação * 
console.log(3.2 * 5);
// Divisão / 
console.log(12 / 2);
// Soma + 
console.log(34 + 67);
// Subtração -
console.log(34 - 23);  
// Resto da divisão %
let remainder;
remainder = 11 % 3;
console.log(remainder);
// Incremento ++
let increment = 0;
increment++;
increment++;
console.log(increment);
// Decremento --
let decrement = 0;
console.log(--decrement);
// Exponencial ** elevado
console.log(3 ** 3);